      def result_2d6(total, dice_total, _dice_list, cmp_op, target)
        return nil unless cmp_op == :>=

        if dice_total <= 2
          Result.fumble("ファンブル")
        elsif dice_total >= 12
          Result.critical(SPECIAL)
        elsif target == "?"
          nil
        elsif total >= target
          Result.success("成功")
        else
          Result.failure("失敗")
        end
      end

